import sys
import face_recognition

#Pass paths to the two images as console parameters
path_img_sample = sys.argv[1]
path_img_template = sys.argv[2]

# Extract biometric features from both images
sample_image = face_recognition.load_image_file(path_img_sample)
sample_face_encoding = face_recognition.face_encodings(sample_image)[0]

template_image = face_recognition.load_image_file(path_img_template)
template_face_encoding = face_recognition.face_encodings(template_image)[0]

# Test the matcher with different tolerance parameters
for tolerance in [0.4, 0.5, 0.6]:
    results = face_recognition.compare_faces([sample_face_encoding], template_face_encoding, tolerance=tolerance)
    
    if results[0] == True:
        print("Accept with tolerance", tolerance)
    else:
        print("Reject with tolerance", tolerance)
